package com.bteam.project.home.model;

public class Traffic {

    private String num;
    private String typeOfAccident;
    private String streetName;
    private String detail;
    private String time;

    public String getNum() {
        return num;
    }

    public void setNum(String num) {
        this.num = num;
    }

    public String getTypeOfAccident() {
        return typeOfAccident;
    }

    public void setTypeOfAccident(String typeOfAccident) {
        this.typeOfAccident = typeOfAccident;
    }

    public String getStreetName() {
        return streetName;
    }

    public void setStreetName(String streetName) {
        this.streetName = streetName;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
